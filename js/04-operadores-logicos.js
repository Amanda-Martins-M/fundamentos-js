// Os operadores lógicos são usados para combinar expressões booleanas, ou seja, sempre que comparamos valores usamos operadores lógicos para determinar se a expressão é verdadeira ou falsa.

// O mais comuns são:
// && (AND) - E = A e B têm que ser ambos verdadeiros (1) para x tambem ser verdadeiro (1);
// || (OR) - OU = Basta uma das variáveis ser verdadeira para X (1);
// ! (NOT) - NÃO = Inverte 0 para 1 e vice-versa.